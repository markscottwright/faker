class usernamefaker : public fakerbase {
    fakerp firstname;
    fakerp lastname;
public:
    usernamefaker(fakerp firstname, fakerp lastname)
        : firstname(firstname), lastname(lastname) {}
    virtual void write(ostream& o) {
        ostringstream firststr, laststr;
        firstname->write(firststr);
        lastname->write(laststr);
        o << (char) tolower(firststr.str()[0]);
        int maxn=7, i=0;
        for (auto c : laststr.str()) {
            if (isalpha(c)) {
                o << (char) tolower(c);
                if (i++ > maxn)
                    break;
            }
        }
    }
};

class domainnamefaker : public fakerbase {
    fakerp company;
public:
    domainnamefaker(fakerp company) : company(company) {}
    virtual void write(ostream& o) {
        ostringstream companystr;
        company->write(companystr);
        char lastc = '-';
        for (auto c : companystr.str()) {
            if (isalnum(c)) {
                o << (char) tolower(c);
            }
            else if (isalnum(lastc)) {
                o << "-";
            }
            lastc = c;
        }
    }
};

class unixtimefaker : public fakerbase {
public:
    virtual void write(ostream& o) {
        time_t randtime = rand() % time(NULL);
        o << (int) randtime;
    }
};

class datefaker : public fakerbase {
public:
    virtual void write(ostream& o) {
        time_t randtime = rand() % time(NULL);
        char buffer[20];
        strftime(buffer, 20, "%F", localtime(&randtime));
        o << buffer;
    }
};


class faker {
protected:
    map<string,fakerp> fakers;

public:
    faker() {
        // use C++ high resolution clock so we can call this more than once per
        // second and still get different results.
        using namespace std::chrono;
        srand(duration_cast<milliseconds>(
           system_clock::now().time_since_epoch()).count());
        #define reg(x) fakers[#x] = x()
        reg(unixtime);
    }
    virtual fakerp operator[](string n) {
        auto it = fakers.find(n);
        if (it == fakers.end())
            return str("");
        return it->second;
    }


    fakerp digit() {return make_shared<digitfaker>();}
    fakerp digits(int n) {return make_shared<digitsfaker>(n);}
    fakerp posdigits(int n) {return make_shared<posdigitsfaker>(n);}
    fakerp maxdigits(int n) {return make_shared<digitsfaker>(n);}
    fakerp str(string s) {return make_shared<strfaker>(s);}
    fakerp combine(vector<fakerp> elems) {
        return make_shared<combinefaker>(elems);
    }
    fakerp combine(fakerp e1) {
        auto elems = {e1};
        return make_shared<combinefaker>(elems);
    }
    fakerp combine(fakerp e1, fakerp e2) {
        auto elems = {e1, e2};
        return make_shared<combinefaker>(elems);
    }
    fakerp combine(fakerp e1, fakerp e2, fakerp e3) {
        auto elems = {e1, e2, e3};
        return make_shared<combinefaker>(elems);
    }
    fakerp combine(fakerp e1, fakerp e2, fakerp e3, fakerp e4) {
        auto elems = {e1, e2, e3, e4};
        return make_shared<combinefaker>(elems);
    }
    fakerp combine(fakerp e1, fakerp e2, fakerp e3, fakerp e4, fakerp e5) {
        auto elems = {e1, e2, e3, e4, e5};
        return make_shared<combinefaker>(elems);
    }

    fakerp combine(string delimiter, vector<fakerp> elems) {
        return make_shared<combinefaker>(delimiter, elems);
    }
    fakerp combine(string delimiter, fakerp e1) {
        auto elems = {e1};
        return make_shared<combinefaker>(delimiter, elems);
    }
    fakerp combine(string delimiter, fakerp e1, fakerp e2) {
        auto elems = {e1, e2};
        return make_shared<combinefaker>(delimiter, elems);
    }
    fakerp combine(string delimiter, fakerp e1, fakerp e2, fakerp e3) {
        auto elems = {e1, e2, e3};
        return make_shared<combinefaker>(delimiter, elems);
    }
    fakerp combine(string delimiter, fakerp e1, fakerp e2, fakerp e3, fakerp e4) {
        auto elems = {e1, e2, e3, e4};
        return make_shared<combinefaker>(delimiter, elems);
    }

    fakerp oneof(vector<string> elems) {
        return make_shared<oneofstrfaker>(elems);
    }
    fakerp oneof(vector<fakerp> elems) {
        return make_shared<oneoffaker>(elems);
    }
    fakerp oneof(fakerp e1) {
        auto elems = {e1};
        return make_shared<oneoffaker>(elems);
    }
    fakerp oneof(fakerp e1, fakerp e2) {
        auto elems = {e1, e2};
        return make_shared<oneoffaker>(elems);
    }
    fakerp oneof(fakerp e1, fakerp e2, fakerp e3) {
        auto elems = {e1, e2, e3};
        return make_shared<oneoffaker>(elems);
    }
    fakerp oneof(fakerp e1, fakerp e2, fakerp e3, fakerp e4) {
        auto elems = {e1, e2, e3, e4};
        return make_shared<oneoffaker>(elems);
    }
    fakerp oneof(fakerp e1, fakerp e2, fakerp e3, fakerp e4, fakerp e5) {
        auto elems = {e1, e2, e3, e4, e5};
        return make_shared<oneoffaker>(elems);
    }
    fakerp oneofchar(string elems) {
        return make_shared<oneofcharfaker>(elems);
    }
    fakerp templ(string templ) {
        return make_shared<templatefaker>(templ);
    }
    fakerp unixtime() {
        return make_shared<unixtimefaker>();
    }
};

